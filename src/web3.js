export const getWeb3 = async () => {
  if (window.ethereum) {
    const web3 = new Web3(window.ethereum);
    console.log("Web3 initialized:", web3);
    return web3;
  } else {
    console.error("No web3 provider detected. Ensure MetaMask is installed.");
    throw new Error("No web3 provider detected");
  }
};

export const getContract = async (web3, network) => {
  if (!web3) {
    console.error("Web3 is not initialized");
    throw new Error("Web3 is not initialized");
  }
  try {
    const { contractAddress, contractABI } = networks[network];
    if (!contractAddress || !contractABI || contractABI.length === 0) {
      console.error("Invalid contract configuration for network:", network);
      throw new Error("Invalid contract configuration");
    }
    const contract = new web3.eth.Contract(contractABI, contractAddress);
    console.log("Contract initialized:", contractAddress);
    return contract;
  } catch (error) {
    console.error("Contract initialization failed:", error);
    throw new Error(`Failed to initialize contract: ${error.message}`);
  }
};

export const getTokenContract = async (web3, network) => {
  if (!web3) {
    console.error("Web3 is not initialized");
    throw new Error("Web3 is not initialized");
  }
  try {
    const tokenAddress = network === "ethereum" ? vPLSAddress : plsxAddress;
    const tokenABI = network === "ethereum" ? vPLSABI : plsxABI;
    const fallbackTokenABI = [
      {
        constant: true,
        inputs: [{ name: "_owner", type: "address" }],
        name: "balanceOf",
        outputs: [{ name: "balance", type: "uint256" }],
        type: "function",
      },
      {
        constant: false,
        inputs: [
          { name: "_spender", type: "address" },
          { name: "_value", type: "uint256" },
        ],
        name: "approve",
        outputs: [{ name: "success", type: "bool" }],
        type: "function",
      },
    ];

    const contract = new web3.eth.Contract(
      tokenABI.length > 0 ? tokenABI : fallbackTokenABI,
      tokenAddress
    );
    console.log("Token contract initialized:", tokenAddress);
    return contract;
  } catch (error) {
    console.error("Token contract initialization failed:", error);
    throw new Error(`Failed to initialize token contract: ${error.message}`);
  }
};

export const switchNetwork = async (network) => {
  const { chainId, chainName, rpcUrls, blockExplorerUrls, nativeCurrency } = networks[network];
  try {
    await window.ethereum.request({
      method: "wallet_switchEthereumChain",
      params: [{ chainId }],
    });
    console.log(`Switched to ${chainName} (chainId: ${chainId})`);
  } catch (switchError) {
    console.error(`Switch to ${chainName} failed:`, switchError);
    if (switchError.code === 4902 || switchError.code === -32603) {
      try {
        await window.ethereum.request({
          method: "wallet_addEthereumChain",
          params: [
            {
              chainId,
              chainName,
              rpcUrls,
              blockExplorerUrls,
              nativeCurrency,
            },
          ],
        });
        console.log(`Added and switched to ${chainName}`);
      } catch (addError) {
        console.error(`Failed to add ${chainName}:`, addError);
        throw new Error(`Failed to add ${chainName}: ${addError.message}`);
      }
    } else {
      throw new Error(`Failed to switch to ${chainName}: ${switchError.message}`);
    }
  }
};
